def calculate_sum():
    """
    Считает сумму двух чисел и выводит результат
    """
    # Задаем числа напрямую в коде
    num1 = 10
    num2 = 5
    
    # Считаем сумму
    result = num1 + num2
    
    # Выводим результат
    print(f"Сумма {num1} и {num2} равна {result}")
